namespace MvcExtensions
{
    using System;
    using System.Diagnostics;

    internal static class Maybe
    {
        [DebuggerStepThrough]
        public static TResult With<TInput, TResult>(this TInput o, Func<TInput, TResult> evaluator)
            where TResult : class
            where TInput : class
        {
            return o == null ? null : evaluator(o);
        }
    }
}
